package Aulas.Fundamentos.Atribuicao;

public class OperadorAtribuicao {

  public static void main(String[] args) {
    int total = 30;

    total += 10;
    System.out.println(total);

    total -= 10;
    System.out.println(total);

    total *= 10;
    System.out.println(total);

    total /= 10;
    System.out.println(total);
  }
}